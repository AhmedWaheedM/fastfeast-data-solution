CREATE TABLE DIM_DRIVER (
    DRIVER_SK INTEGER PRIMARY KEY,
    DRIVER_ID INTEGER,
    HASHED_DRIVER_NAME VARCHAR,
    HASHED_DRIVER_PHONE VARCHAR,
    HASHED_NATIONAL_ID VARCHAR,
    SHIFT VARCHAR,
    VEHICLE_TYPE VARCHAR,
    HIRE_DATE DATE,
    RATING_AVG DECIMAL(3,2),
    ON_TIME_RATE DECIMAL(5,2),
    CANCEL_RATE DECIMAL(5,2),
    COMPLETED_DELIVERIES INTEGER,
    IS_ACTIVE BOOLEAN,
    CITY_NAME VARCHAR,
    REGION_NAME VARCHAR,
    COUNTRY VARCHAR,
    TIMEZONE VARCHAR,
    DELIVERY_BASE_FEE DECIMAL(18,2),
    ROW_EFFECTIVE_TS TIMESTAMP
);
